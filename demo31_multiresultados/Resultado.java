// Resultado - Armazena o nome e o valor resultante de uma tarefa
public class Resultado {
    private String nome; // Nome da tarefa
    private int valor; // Valor calculado pela tarefa

    // Retorna o nome do resultado
    public String getNome() {
        return nome;
    }

    // Define o nome do resultado
    public void setNome(String nome) {
        this.nome = nome;
    }

    // Retorna o valor do resultado
    public int getValor() {
        return valor;
    }

    // Define o valor do resultado
    public void setValor(int valor) {
        this.valor = valor;
    }
}
